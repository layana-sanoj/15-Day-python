#counting the how many times a word appears in a sentence
#the sentence is taken as input from the user
#the sentence used here is "this is a test sentence and this is just a test"
counts=dict()
sentence=input("Enter a sentence: ")
words=sentence.split()
print("Words in the sentence are: ", words)
print("Counting the words...")
for word in words:
    counts[word] = counts.get(word, 0) + 1
print("Word counts: ", counts)
